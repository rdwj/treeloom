"""Shared fixtures for treeloom benchmarks."""

from __future__ import annotations

import pytest

from treeloom import CodePropertyGraph, CPGBuilder, NodeKind, TaintLabel, TaintPolicy
from treeloom.model.nodes import CpgNode


def _generate_python_source(num_classes: int, methods_per_class: int) -> bytes:
    """Generate deterministic synthetic Python source.

    Produces classes with methods that assign variables, call each other,
    branch on conditions, and return values — giving tree-sitter a realistic
    parse workload and treeloom a non-trivial graph to build.
    """
    lines: list[str] = [
        "# Synthetic benchmark source — generated by treeloom benchmarks",
        "",
        "import os",
        "import sys",
        "",
    ]

    # Emit a handful of standalone helper functions first so methods have
    # something external to call.
    for i in range(5):
        lines += [
            f"def helper_{i}(value, flag=False):",
            f"    result = value * {i + 1}",
            "    if flag:",
            f"        result = result + {i}",
            "    return result",
            "",
        ]

    for cls_idx in range(num_classes):
        lines += [
            f"class Worker{cls_idx}:",
            f'    """Worker class number {cls_idx}."""',
            "",
            "    def __init__(self, name, config):",
            "        self.name = name",
            "        self.config = config",
            f"        self._state = {cls_idx}",
            "",
        ]

        for meth_idx in range(methods_per_class):
            param_a = f"param_{meth_idx}_a"
            param_b = f"param_{meth_idx}_b"
            local_var = f"local_{meth_idx}"
            result_var = f"result_{meth_idx}"
            helper_idx = (cls_idx + meth_idx) % 5

            lines += [
                f"    def method_{meth_idx}(self, {param_a}, {param_b}=None):",
                f"        {local_var} = {param_a}",
                f"        if {param_b} is not None:",
                f"            {local_var} = helper_{helper_idx}({param_a}, flag=True)",
                f"        {result_var} = {local_var}",
                f"        for i in range({meth_idx + 1}):",
                f"            {result_var} = {result_var}",
                f"        return {result_var}",
                "",
            ]

        # Add a method that calls sibling methods so intra-class call edges form.
        lines += [
            "    def run(self, data):",
            "        output = data",
        ]
        for meth_idx in range(min(methods_per_class, 3)):
            lines.append(f"        output = self.method_{meth_idx}(output)")
        lines += [
            "        return output",
            "",
        ]

    return "\n".join(lines).encode()


def _build_cpg(source: bytes) -> CodePropertyGraph:
    return CPGBuilder().add_source(source, "synthetic.py", "python").build()


