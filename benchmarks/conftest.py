# ---------------------------------------------------------------------------
# Taint policy fixture
# ---------------------------------------------------------------------------


@pytest.fixture(scope="session")
def taint_policy() -> TaintPolicy:
    """Simple policy: PARAMETER nodes are sources, CALL nodes are sinks."""

    def sources(node: CpgNode) -> TaintLabel | None:
        if node.kind == NodeKind.PARAMETER:
            return TaintLabel(name="param_taint", origin=node.id)
        return None

    def sinks(node: CpgNode) -> bool:
        return node.kind == NodeKind.CALL

    def sanitizers(node: CpgNode) -> bool:
        return False

    return TaintPolicy(sources=sources, sinks=sinks, sanitizers=sanitizers)
